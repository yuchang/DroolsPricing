package com.capgemini.ate.pricing.bo;

public class Competitor {
	
	private String name;
	private String zone;
	private String sizeCategory;
	private String alignmentStrategy;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getZone() {
		return zone;
	}
	
	public void setZone(String zone) {
		this.zone = zone;
	}
	
	public String getSizeCategory() {
		return sizeCategory;
	}
	
	public void setSizeCategory(String sizeCategory) {
		this.sizeCategory = sizeCategory;
	}
	
	public String getAlignmentStrategy() {
		return alignmentStrategy;
	}
	
	public void setAlignmentStrategy(String alignmentStrategy) {
		this.alignmentStrategy = alignmentStrategy;
	}

}